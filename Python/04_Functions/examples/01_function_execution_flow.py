# File: 01_function_execution_flow.py

def greet():
    print("Good Morning")

print("Program Started")

greet()

print("Program Finished")

"""
Concept:
Function Execution Flow

Learning Points:
1. Python reads the program from top to bottom.
2. A function definition does NOT execute the function.
3. A function executes only when it is called.
4. After the function finishes, control returns to the next line after the function call.
"""